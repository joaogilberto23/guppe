"""
Tipos numéricos

5 // 2 = 2    =>   Barras duplas apresentam resultado inteiro;

3 ** 3 = 27   =>   Asterisco duplos elevam o numero a potência do número seguinte;

Em Python não existe um número máximo como no Java, por  exemplo; o limite é a mémoria do computador;

Permite divisão de unidades com underline, ex: 100 = 1_00; 1_000_000 = 1000000.

"""
num = 10
print(num)
num += 2    # num = num + 2
print(num)
num -= 2    # num = num - 2
print(num)
num *= 2    # num = num * 2
print(num)
num /= 3    # num = num / 3
print(num)
num //= 2   # num = num // 2
print(num)
